"""
Streamlit 대시보드의 메인(홈) 페이지.
버거지수 분석 프로젝트의 개요를 설명하고, 좌측 사이드바를 통해 다른 페이지로 이동할 수 있음을 안내합니다.
"""
import streamlit as st

st.set_page_config(
    page_title="버거지수 분석 대시보드",
    page_icon="🍔",
    layout="wide"
)

def main():
    st.title("🍔 지역별 버거지수 분석 대시보드")
    st.markdown("""
    이 대시보드는 대한민국의 주요 패스트푸드 브랜드(버거킹, 맥도날드, KFC, 롯데리아) 매장 분포를 바탕으로 산출된 **'버거지수'**를 분석하고 시각화합니다.
    
    ### 📌 버거지수(Burger Index)란?
    `(버거킹 + 맥도날드 + KFC) / 롯데리아` 의 공식으로 산출되며, 도시의 발전도나 접근성을 간접적으로 비교하는 데 사용되는 재미있는 지표입니다.
    
    👈 **좌측 사이드바 메뉴**를 통해 다음 기능들을 확인할 수 있습니다:
    
    1. **01 EDA**: 버거지수 및 브랜드별 매장 수의 기술 통계, 기초 분포도 시각화를 제공합니다.
    2. **02 Folium Scatter**: Folium을 활용하여 전국 시군구 위경도 좌표에 버거지수를 원형 마커로 시각화합니다.
    3. **03 Folium Choropleth**: 행정구역(GeoJSON)을 기준으로 지역별 버거지수를 단계구분도로 렌더링합니다.
    4. **04 Cartogram**: 전국의 시군구를 격자 블록 맵으로 변환하여 버거지수를 한눈에 비교할 수 있는 카토그램을 제공합니다.
    """)

if __name__ == "__main__":
    main()
